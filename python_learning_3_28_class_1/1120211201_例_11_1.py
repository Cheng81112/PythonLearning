def sing_jiandanai():
    #一个函数，实现演唱《简单爱》片段
    print( "======简单爱==begin=======")
    print("说不上为什麼我变得很主动")
    print("若爱上一个人什麼都会值得去做")
    print("我想大声宣布对你依依不舍")
    print( "连隔壁邻居都猜到我现在的感受")
    print("河边的风在吹着头发飘动")
    print("牵着你的手一-阵莫名感动")
    print("我想带你回我的外婆家")
    print("-起看着日落一直到我们都睡着")
    print("... ")
    print("======简单爱==end-======")
    print("")

